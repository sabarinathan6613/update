-- supabase-schema.sql
-- Run this script in your Supabase project's SQL Editor to set up the database structure.

-- Enable UUID extension
create extension if not exists "uuid-ossp";

-- 1. Plants table
create table public.plants (
    id text primary key,
    name text not null,
    location text not null,
    capacity integer not null,
    target_oee integer not null
);

-- Seed plants
insert into public.plants (id, name, location, capacity, target_oee) values
('plant-1', 'Detroit Engine Plant', 'Detroit, MI', 5000, 85),
('plant-2', 'Munich Assembly', 'Munich, Germany', 3200, 90),
('plant-3', 'Tokyo Assembly', 'Tokyo, Japan', 4500, 88)
on conflict (id) do update set
    name = excluded.name,
    location = excluded.location,
    capacity = excluded.capacity,
    target_oee = excluded.target_oee;

-- 2. User Profiles table (maps auth.users to roles and plants)
create table public.profiles (
    id uuid references auth.users on delete cascade primary key,
    email text unique not null,
    name text not null,
    role text not null check (role in ('Super Admin', 'Plant Admin', 'User', 'Admin', 'Viewer')),
    plant_id text not null, -- 'all' for Super Admin, or a specific plant_id
    active boolean not null default true
);

-- 3. Production Data table
create table public.production_data (
    id text primary key, -- composite: plant_id + timestamp
    plant_id text references public.plants(id) on delete cascade,
    timestamp timestamp with time zone not null,
    date text not null,
    hour integer not null,
    shift text not null,
    target_parts integer not null,
    actual_parts integer not null,
    reject_parts integer not null,
    uptime_minutes integer not null,
    downtime_reason text
);

-- Add index on plant_id and date for fast dashboard/trends queries
create index idx_prod_plant_date on public.production_data(plant_id, date);
create index idx_prod_timestamp on public.production_data(timestamp);

-- 4. Report History table
create table public.report_history (
    id text primary key,
    name text not null,
    type text not null,
    date_range text not null,
    shift text not null,
    plant_id text not null,
    generated_at timestamp with time zone default now(),
    created_by text
);

-- 5. Scheduled Reports table
create table public.scheduled_reports (
    id text primary key,
    plant_id text not null,
    report_type text not null,
    frequency text not null,
    time text not null,
    email_recipients text not null,
    enabled boolean not null default true,
    last_run timestamp with time zone,
    format_pdf boolean not null default true,
    format_excel boolean not null default true
);

