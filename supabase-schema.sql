-- Helper functions for database schema dynamic discovery
CREATE OR REPLACE FUNCTION public.count_rows(schema_name text, table_name text)
RETURNS bigint SECURITY DEFINER AS $$
DECLARE
    result bigint;
    query text;
BEGIN
    query := 'SELECT count(*) FROM ' || quote_ident(schema_name) || '.' || quote_ident(table_name);
    EXECUTE query INTO result;
    RETURN result;
EXCEPTION
    WHEN OTHERS THEN
        RETURN 0;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION public.discover_database_structure()
RETURNS json SECURITY DEFINER AS $$
DECLARE
    result json;
BEGIN
    SELECT json_build_object(
        'tables', COALESCE(
            (
                SELECT json_agg(
                    json_build_object(
                        'name', t.table_name::text,
                        'schema', t.table_schema::text,
                        'recordCount', public.count_rows(t.table_schema, t.table_name),
                        'primaryKey', COALESCE(
                            (
                                SELECT kcu.column_name::text
                                FROM information_schema.table_constraints tc
                                JOIN information_schema.key_column_usage kcu 
                                  ON tc.constraint_name = kcu.constraint_name
                                  AND tc.table_schema = kcu.table_schema
                                WHERE tc.constraint_type = 'PRIMARY KEY'
                                  AND tc.table_schema = t.table_schema
                                  AND tc.table_name = t.table_name
                                LIMIT 1
                            ), 'id'
                        ),
                        'status', 'ACTIVE',
                        'columns', COALESCE(
                            (
                                SELECT json_agg(
                                    json_build_object(
                                        'name', c.column_name::text,
                                        'type', c.data_type::text,
                                        'isPk', COALESCE(
                                            (
                                                SELECT true
                                                FROM information_schema.table_constraints tc
                                                JOIN information_schema.key_column_usage kcu 
                                                  ON tc.constraint_name = kcu.constraint_name
                                                  AND tc.table_schema = kcu.table_schema
                                                WHERE tc.constraint_type = 'PRIMARY KEY'
                                                  AND tc.table_schema = c.table_schema
                                                  AND tc.table_name = c.table_name
                                                  AND kcu.column_name = c.column_name
                                                LIMIT 1
                                            ), false
                                        )
                                    )
                                )
                                FROM information_schema.columns c
                                WHERE c.table_schema = t.table_schema
                                  AND c.table_name = t.table_name
                            ), '[]'::json
                        )
                    )
                )
                FROM information_schema.tables t
                WHERE t.table_schema = 'public'
                  AND t.table_type = 'BASE TABLE'
            ), '[]'::json
        )
    ) INTO result;
    
    RETURN result;
END;
$$ LANGUAGE plpgsql;

GRANT EXECUTE ON FUNCTION public.count_rows(text, text) TO anon, authenticated;
GRANT EXECUTE ON FUNCTION public.discover_database_structure() TO anon, authenticated;

-- 10. Automatically Create User Profile on Auth SignUp / Admin User Creation
CREATE OR REPLACE FUNCTION public.handle_new_user_admin()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
    INSERT INTO public.profiles (id, email, name, role, plant_id, active)
    VALUES (
        new.id,
        new.email,
        COALESCE(new.raw_user_meta_data->>'name', ''),
        COALESCE(new.raw_user_meta_data->>'role', 'User'),
        COALESCE(new.raw_user_meta_data->>'plant_id', 'plant-1'),
        COALESCE((new.raw_user_meta_data->>'active')::boolean, true)
    );
    RETURN new;
END;
$$;

DROP TRIGGER IF EXISTS on_auth_user_created_admin ON auth.users;

CREATE TRIGGER on_auth_user_created_admin
AFTER INSERT ON auth.users
FOR EACH ROW EXECUTE FUNCTION public.handle_new_user_admin();

